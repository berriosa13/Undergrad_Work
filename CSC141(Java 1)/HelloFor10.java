//demonstrates a simple for loop

public class HelloFor10
{
   public static void main(String[]args)
   {
      final int n = 5;
      final String message = "hello";
      final double delay = 0.3;
      
      for (int x = 1; x <= n; x++)
      {
         System.out.printf("%2d: %s\n", x, message);
         Wait.pause(delay);
      }     
      
   
   
   
   
   
   
   
   
   
   
   
   }
  
   
}   
   
   
   
