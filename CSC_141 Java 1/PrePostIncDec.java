public class PrePostIncDec
{
   public static void main(String [] args)
   {
      int x = 3;
      
      while (x > 0)
      { 
         System.out.println(++x);
         System.out.println(x++);
         System.out.println(--x);
         System.out.println(x--);
         System.out.println(++x);
         System.out.println(--x);
         System.out.println(x++);
         --x;
         break;
         
      }   
   
   
   
   
   }
   
   
   
   
}